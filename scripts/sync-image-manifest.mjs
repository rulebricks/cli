#!/usr/bin/env node
// Syncs the helm repo's images/manifest.yaml into the CLI as a bundled
// OFFLINE FALLBACK snapshot (src/generated/imageManifest.ts).
//
// The snapshot is NOT the source of truth: at deploy time the CLI resolves the
// manifest for the exact chart version being installed (from the chart tarball
// itself — see src/lib/imageCatalog.ts). Staleness here only affects offline
// `rulebricks init` previews, which the next `deploy` regenerates with live
// data. Refresh opportunistically (e.g. at CLI release time); no lock-step
// with the helm repo is required.
//
// The Helm chart repo is the source of truth. Point this at it via:
//   node scripts/sync-image-manifest.mjs --from=/path/to/helm
//   RULEBRICKS_HELM_DIR=/path/to/helm node scripts/sync-image-manifest.mjs
//   node scripts/sync-image-manifest.mjs --from=/path/to/images/manifest.yaml
//
// Defaults to ../helm relative to the CLI repo.
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import YAML from "yaml";

const cliRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");

const fromArg = process.argv
  .find((arg) => arg.startsWith("--from="))
  ?.slice("--from=".length);
const source =
  fromArg || process.env.RULEBRICKS_HELM_DIR || path.resolve(cliRoot, "../helm");

function resolveManifestPath(input) {
  const resolved = path.resolve(input);
  if (fs.existsSync(resolved) && fs.statSync(resolved).isDirectory()) {
    return path.join(resolved, "images", "manifest.yaml");
  }
  return resolved;
}

const manifestPath = resolveManifestPath(source);
if (!fs.existsSync(manifestPath)) {
  console.error(`Image manifest not found at ${manifestPath}`);
  console.error(
    "Pass --from=<helm repo or images/manifest.yaml> or set RULEBRICKS_HELM_DIR.",
  );
  process.exit(1);
}

const raw = fs.readFileSync(manifestPath, "utf8");
const parsed = YAML.parse(raw);
if (!parsed || !Array.isArray(parsed.images)) {
  console.error(`${manifestPath} does not contain an images: list.`);
  process.exit(1);
}

// Keep only the fields the CLI consumes (name/tag/target/digest); kind, source
// and auth are mirror-pipeline concerns.
const images = parsed.images.map((entry) => {
  if (!entry || typeof entry.name !== "string" || typeof entry.tag !== "string") {
    console.error(
      `Malformed manifest entry (name/tag must be strings): ${JSON.stringify(entry)}`,
    );
    process.exit(1);
  }
  const slim = { name: entry.name, tag: entry.tag };
  if (typeof entry.target === "string" && entry.target) slim.target = entry.target;
  if (typeof entry.digest === "string" && entry.digest) slim.digest = entry.digest;
  return slim;
});

// Guard: every image name the CLI itself references must exist. Keep in sync
// with REQUIRED_IMAGE_NAMES in src/lib/imageCatalog.ts.
const required = [
  "curl",
  "hyperdx",
  "clickstack-otel-collector",
  "ferretdb",
  "postgres-documentdb",
  "opentelemetry-collector",
  "kafka-proxy",
  "supabase-postgres",
  "rclone",
  "strimzi-kafka",
];
const names = new Set(images.map((entry) => entry.name));
const missing = required.filter((name) => !names.has(name));
if (missing.length > 0) {
  console.error(
    `Manifest is missing entries the CLI depends on: ${missing.join(", ")}`,
  );
  process.exit(1);
}

const destDir = path.join(cliRoot, "src", "generated");
const destPath = path.join(destDir, "imageManifest.ts");
fs.mkdirSync(destDir, { recursive: true });

const banner = [
  "// AUTO-GENERATED by scripts/sync-image-manifest.mjs from the helm repo's",
  "// images/manifest.yaml — do not edit by hand. Run `npm run sync-images`.",
  "//",
  "// This is the OFFLINE FALLBACK only. At deploy time the CLI resolves the",
  "// manifest for the exact chart version being installed (see",
  "// src/lib/imageCatalog.ts), so staleness here never reaches a cluster.",
].join("\n");

fs.writeFileSync(
  destPath,
  `${banner}\nexport const BUNDLED_IMAGE_MANIFEST: {\n  images: Array<{ name: string; tag: string; target?: string; digest?: string }>;\n} = ${JSON.stringify({ images }, null, 2)};\n`,
);

console.log(
  `Synced image manifest snapshot (${images.length} entries):\n  from ${manifestPath}\n  to   ${destPath}`,
);
